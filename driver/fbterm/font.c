static struct glyph *make_glyph(const char *str, int w, int h) {
	struct glyph *glyph;
	int i;

	glyph = malloc(sizeof(glyph) * sizeof(uint8_t) * w * h);
	glyph->w = w;
	glyph->h = h;

	for (i = 0; i < w * h; i++) {
		if (str[i] == '*') {
			glyph->value[i] = 0xFF;
		}
		else {
			glyph->value[i] = 0x00;
		}
	}

	return glyph;
}

struct font *font_load(const char *path) {
	struct font *font;
	int i;

	font = malloc(sizeof(struct font) + sizeof(struct glyph*) * 128);
	font->count = 128;

	font->w = 6;
	font->h = 10;

	for (i = 0; i < 32; i++) {
		font->glyph[i] = NULL;
	}

	for (i = 32; i < 128; i++) {
		font->glyph[i] = make_glyph(builtin_font[i - 32], 6, 10);
	}
	
	// set default glyph
	font->def_glyph = font->glyph[(size_t) ' '];

	return font;
}
